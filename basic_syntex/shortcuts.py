# Todo some short cuts for doing operations in python

# Todo Addition
a = b = 10

a = a + 10
b += 10
"""
    both operations will return same result
"""
print("Sum A = " + str(a) + " & B = " + str(b))

# Todo Subtraction
a = b = 10

a = a - 10
b -= 10
"""
    both operations will return same result
"""
print("Subtraction A = " + str(a) + " & B = " + str(b))

# Todo Multiplication
